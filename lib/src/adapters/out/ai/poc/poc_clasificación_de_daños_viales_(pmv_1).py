# ============================================
# BLOQUE EXTRA: LIMPIEZA Y ESTANDARIZACIÓN DE IMÁGENES
# Objetivo:
# Convertir todas las imágenes del dataset a
# formato JPG limpio y RGB para evitar errores
# de lectura en TensorFlow.
# ============================================

