origen = "/content/dataset_vial"
destino_limpio = "/content/dataset_vial_limpio"

# Crear estructura limpia
for split in ["train", "val", "test"]:
    for clase in clases:
        os.makedirs(os.path.join(destino_limpio, split, clase), exist_ok=True)

# Convertir imágenes
contador_convertidas = 0

for split in ["train", "val", "test"]:
    for clase in clases:
        ruta_origen = os.path.join(origen, split, clase)
        ruta_destino = os.path.join(destino_limpio, split, clase)

        for i, archivo in enumerate(os.listdir(ruta_origen)):
            ruta_archivo = os.path.join(ruta_origen, archivo)

            try:
                with Image.open(ruta_archivo) as img:
                    img = img.convert("RGB")  # asegurar 3 canales
                    nuevo_nombre = f"{clase}_{i+1}.jpg"
                    ruta_salida = os.path.join(ruta_destino, nuevo_nombre)
                    img.save(ruta_salida, "JPEG")
                    contador_convertidas += 1

            except Exception as e:
                print(f"No se pudo convertir: {ruta_archivo}")

print(f"\nConversión completada. Total de imágenes convertidas: {contador_convertidas}")
