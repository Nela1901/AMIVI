# ============================================

# Tomar un lote del dataset de entrenamiento
for image_batch, label_batch in train_ds.take(1):
    print("Forma del lote de imágenes:", image_batch.shape)
    print("Forma del lote de etiquetas:", label_batch.shape)

"""**BLOQUE 8 – PREPROCESAMIENTO Y DATA AUGMENTATION**"""

# ============================================
# BLOQUE 8.1: DATA AUGMENTATION
# Objetivo:
# Crear una capa que aplique transformaciones
# aleatorias a las imágenes durante el
# entrenamiento para mejorar la generalización.
# ============================================

data_augmentation = tf.keras.Sequential([
    layers.RandomFlip("horizontal"),
    layers.RandomRotation(0.2),
    layers.RandomZoom(0.2),
    layers.RandomTranslation(0.15, 0.15),
    layers.RandomContrast(0.2),
])

# ============================================
# BLOQUE 8.2: PREPROCESAMIENTO DE ENTRADA
# Objetivo:
# Aplicar el preprocesamiento requerido por
# MobileNetV2 antes de enviar las imágenes
# al modelo.
# ============================================

# Función de preprocesamiento oficial de MobileNetV2
preprocess_layer = preprocess_input

# ============================================
# BLOQUE 8.3: VISUALIZACIÓN DEL DATA AUGMENTATION
# Objetivo:
# Mostrar ejemplos de cómo una imagen puede
# transformarse automáticamente durante el
# entrenamiento.
# ============================================

plt.figure(figsize=(12, 12))

for images, labels in train_ds.take(1):
    imagen_ejemplo = images[0]

    for i in range(9):
        ax = plt.subplot(3, 3, i + 1)
        imagen_aumentada = data_augmentation(tf.expand_dims(imagen_ejemplo, 0))
        plt.imshow(imagen_aumentada[0].numpy().astype("uint8"))
        plt.axis("off")

plt.tight_layout()
plt.show()

"""**BLOQUE 9 – OPTIMIZACIÓN DEL DATASET**"""

# ============================================
# BLOQUE 9: OPTIMIZACIÓN DEL DATASET
# Objetivo:
# Mejorar el rendimiento del entrenamiento
# utilizando cache y prefetch para optimizar
# la carga de datos en TensorFlow.
# ============================================

AUTOTUNE = tf.data.AUTOTUNE

train_ds = train_ds.cache().prefetch(buffer_size=AUTOTUNE)
val_ds = val_ds.cache().prefetch(buffer_size=AUTOTUNE)
test_ds = test_ds.cache().prefetch(buffer_size=AUTOTUNE)

print("Datasets optimizados correctamente.")

"""**BLOQUE 10 – CONSTRUCCIÓN DEL MODELO**"""

# ============================================
# BLOQUE 10: CONSTRUCCIÓN DEL MODELO
# Objetivo:
# Construir un modelo de clasificación de
# imágenes basado en MobileNetV2 mediante
# Transfer Learning.
# ============================================

# Cargar modelo base preentrenado
base_model = MobileNetV2(
    input_shape=(224, 224, 3),
    include_top=False,
    weights="imagenet"
)

# Congelar el modelo base
base_model.trainable = False

# Construcción del modelo completo
inputs = tf.keras.Input(shape=(224, 224, 3))

x = data_augmentation(inputs)
x = preprocess_layer(x)
x = base_model(x, training=False)
x = layers.GlobalAveragePooling2D()(x)
x = layers.Dropout(0.2)(x)
outputs = layers.Dense(4, activation="softmax")(x)

model = tf.keras.Model(inputs, outputs)

# Mostrar resumen del modelo
model.summary()

"""**BLOQUE 11 – COMPILACIÓN DEL MODELO**"""

# ============================================
# BLOQUE 11: COMPILACIÓN DEL MODELO
# Objetivo:
# Configurar el modelo definiendo el
# optimizador, la función de pérdida y
# la métrica de evaluación.
# ============================================

model.compile(
    optimizer=tf.keras.optimizers.Adam(learning_rate=0.001),
    loss="categorical_crossentropy",
    metrics=["accuracy"]
)

print("Modelo compilado correctamente.")

# ============================================
# BLOQUE 11.1: FINE-TUNING DEL MODELO
# Objetivo:
# Ajustar parcialmente el modelo MobileNetV2
# para mejorar la adaptación al problema de
# clasificación de daño vial.
# ============================================

# Descongelar el modelo base
base_model.trainable = True

# Congelar las primeras capas (mantener conocimiento general)
for layer in base_model.layers[:120]:
    layer.trainable = False

# Recompilar con learning rate más bajo
model.compile(
    optimizer=tf.keras.optimizers.Adam(learning_rate=0.0001),
    loss="categorical_crossentropy",
    metrics=["accuracy"]
)

print("Fine-tuning aplicado correctamente.")

from sklearn.utils.class_weight import compute_class_weight
import numpy as np

# Obtener etiquetas reales del dataset de entrenamiento
y_train = np.concatenate([y for x, y in train_ds], axis=0)

# Convertir de one-hot a etiquetas (si estás usando categorical)
y_train_labels = np.argmax(y_train, axis=1)

# Calcular pesos
class_weights = compute_class_weight(
    class_weight='balanced',
    classes=np.unique(y_train_labels),
    y=y_train_labels
)

# Convertir a diccionario
class_weights = dict(enumerate(class_weights))

print("Pesos de clase:", class_weights)

"""**BLOQUE 12 – ENTRENAMIENTO DEL MODELO**"""

# ============================================
# BLOQUE 12: ENTRENAMIENTO DEL MODELO
# Objetivo:
# Entrenar el modelo de clasificación usando
# el conjunto de entrenamiento y evaluar su
# desempeño con el conjunto de validación.
# ============================================

epochs = 15

history = model.fit(
    train_ds,
    validation_data=val_ds,
    epochs=15,
    class_weight=class_weights
)

"""**BLOQUE 13 – VISUALIZACIÓN DEL ENTRENAMIENTO**"""

# ============================================
# BLOQUE 13: VISUALIZACIÓN DEL ENTRENAMIENTO
# Objetivo:
# Graficar la evolución de la precisión
# (accuracy) y la pérdida (loss) durante el
# entrenamiento y la validación del modelo.
# ============================================

# Extraer métricas del historial
acc = history.history["accuracy"]
val_acc = history.history["val_accuracy"]
loss = history.history["loss"]
val_loss = history.history["val_loss"]

epochs_range = range(1, len(acc) + 1)

# -----------------------------
# Gráfica de Accuracy
# -----------------------------
plt.figure(figsize=(8, 5))
plt.plot(epochs_range, acc, marker="o", label="Entrenamiento")
plt.plot(epochs_range, val_acc, marker="o", label="Validación")
plt.title("Evolución de la Accuracy")
plt.xlabel("Épocas")
plt.ylabel("Accuracy")
plt.legend()
plt.grid(True)
plt.show()

# -----------------------------
# Gráfica de Loss
# -----------------------------
plt.figure(figsize=(8, 5))
plt.plot(epochs_range, loss, marker="o", label="Entrenamiento")
plt.plot(epochs_range, val_loss, marker="o", label="Validación")
plt.title("Evolución de la Loss")
plt.xlabel("Épocas")
plt.ylabel("Loss")
plt.legend()
plt.grid(True)
plt.show()

"""**BLOQUE 14 – EVALUACIÓN EN TEST**"""

# ============================================
# BLOQUE 14: EVALUACIÓN FINAL EN TEST
# Objetivo:
# Medir el desempeño final del modelo utilizando
# el conjunto de prueba, el cual no fue usado
# durante el entrenamiento ni la validación.
# ============================================

test_loss, test_accuracy = model.evaluate(test_ds)

print(f"\nTest Accuracy: {test_accuracy:.4f}")
print(f"Test Loss: {test_loss:.4f}")

"""**BLOQUE 15 – MATRIZ DE CONFUSIÓN Y REPORTE DE CLASIFICACIÓN**"""

# ============================================
# BLOQUE 15.1: OBTENER PREDICCIONES EN TEST
# Objetivo:
# Generar predicciones del modelo sobre el
# conjunto de prueba y preparar los datos para
# el análisis por clase.
# ============================================

from sklearn.metrics import classification_report, confusion_matrix
import seaborn as sns

# Obtener predicciones del modelo
predicciones = model.predict(test_ds)

# Convertir probabilidades a índice de clase
y_pred = np.argmax(predicciones, axis=1)

# Obtener etiquetas reales
y_true = np.concatenate([np.argmax(labels.numpy(), axis=1) for images, labels in test_ds])

print("Predicciones generadas correctamente.")
print("Cantidad de predicciones:", len(y_pred))
print("Cantidad de etiquetas reales:", len(y_true))

# ============================================
# BLOQUE 15.2: MATRIZ DE CONFUSIÓN
# Objetivo:
