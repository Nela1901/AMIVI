"""**BLOQUE 1 – Preparación y organización del dataset**"""

import os
import random
import shutil

# -----------------------------
# CONFIGURACIÓN
# -----------------------------
origen = "/content/dataset_bruto"
destino = "/content/dataset_vial"

clases = ["normal", "leve", "moderado", "severo"]

# porcentajes
train_pct = 0.70
val_pct = 0.15
test_pct = 0.15

# extensiones válidas
ext_validas = (".jpg", ".jpeg", ".png", ".webp")

# -----------------------------
# CREAR ESTRUCTURA DESTINO
# -----------------------------
for split in ["train", "val", "test"]:
    for clase in clases:
        os.makedirs(os.path.join(destino, split, clase), exist_ok=True)

# -----------------------------
# REPARTIR IMÁGENES
# -----------------------------
for clase in clases:
    carpeta_clase = os.path.join(origen, clase)

    imagenes = [img for img in os.listdir(carpeta_clase) if img.lower().endswith(ext_validas)]
    random.shuffle(imagenes)

    total = len(imagenes)
    n_train = int(total * train_pct)
    n_val = int(total * val_pct)
    n_test = total - n_train - n_val

    train_imgs = imagenes[:n_train]
    val_imgs = imagenes[n_train:n_train+n_val]
    test_imgs = imagenes[n_train+n_val:]

    # copiar archivos
    for img in train_imgs:
        shutil.copy(os.path.join(carpeta_clase, img),
                    os.path.join(destino, "train", clase, img))

    for img in val_imgs:
        shutil.copy(os.path.join(carpeta_clase, img),
                    os.path.join(destino, "val", clase, img))

    for img in test_imgs:
        shutil.copy(os.path.join(carpeta_clase, img),
                    os.path.join(destino, "test", clase, img))

    print(f"Clase: {clase}")
    print(f"  Train: {len(train_imgs)}")
    print(f"  Val:   {len(val_imgs)}")
    print(f"  Test:  {len(test_imgs)}")
    print("-" * 30)

print("Dataset repartido correctamente en /content/dataset_vial")

